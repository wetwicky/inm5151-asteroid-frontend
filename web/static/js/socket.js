function randomName() {
  let i = Math.floor(Math.random() * adjectives.length);
  let j = Math.floor(Math.random() * nouns.length);
  return adjectives[i] + " " + nouns[j];
}

var name = "";
// Now that you are connected, you can join channels with a topic:
let channel = socket.channel("player:default", {});
channel.join()
  .receive("ok", resp => {
    name = randomName();
    console.log("Joined successfully as " + name, resp);

    channel.push("connect", {name: name});
  })
  .receive("error", resp => { console.log("Unable to join", resp) });

